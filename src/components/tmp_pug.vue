<script>
export default {
  name: "HelloWorld",
  computed: {
    data() {
      return this.$store.state.data;
    },
    error() {
      return this.$store.state.error;
    }
  },
  data() {
    return {
      danePogodowe: [],
      city: null,
      color: "white"
    };
  },
  methods: {
    getImagePath() {
      let src = "";
      if (this.data.data) {
        src =
          "https://openweathermap.org/img/wn/" +
          this.data.data.weather[0].icon +
          "@2x.png";
      }
      return src;
    },
    getDate(utc, type) {
      let d = new Date(utc * 1000);
      let utc_ = d.getTime() + d.getTimezoneOffset() * 60000;
      let nd = new Date(utc_);
      let time =
        ("00" + nd.getHours()).slice(-2) +
        ":" +
        ("00" + nd.getMinutes()).slice(-2);
      let date =
        ("00" + nd.getDate()).slice(-2) +
        "." +
        ("00" + (nd.getMonth() + 1)).slice(-2) +
        "." +
        nd.getUTCFullYear();
      if (type === "time") {
        return time;
      } else {
        return date + " " + time;
      }
    },
    searchAPI() {
      this.$store.dispatch("getData", this.city).then(() => {
        this.city = null;
      });
    },
    getLocation() {
      if (navigator.geolocation) {
        navigator.geolocation.getCurrentPosition(this.success);
      } else {
        console.log("Geolocation is not supported by this browser.");
      }
    },
    success(pos) {
      const crd = pos.coords;
      this.$store.dispatch("getDataByLocation", crd);
    }
  },
  mounted() {
    this.getLocation();
  },
  watch: {
    dane: {
      handler() {
        this.danePogodowe = dane;
      },
      deep: true
    }
  }
};
</script>

